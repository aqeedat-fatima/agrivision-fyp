function applyLanguage() {
  const lang = localStorage.getItem("agrivision_lang") || "en";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    el.textContent = I18N[lang]?.[key] || I18N.en[key] || el.textContent;
  });

  const label = document.getElementById("lang-label");
  if (label) label.textContent = lang === "ur" ? "UR" : "EN";

  // ✅ Only this (no dir change)
  document.documentElement.lang = lang === "ur" ? "ur" : "en";
  document.body.classList.toggle("urdu-mode", lang === "ur");
}

function toggleLanguage() {
  const current = localStorage.getItem("agrivision_lang") || "en";
  localStorage.setItem("agrivision_lang", current === "en" ? "ur" : "en");
  applyLanguage();
}

document.addEventListener("DOMContentLoaded", applyLanguage);